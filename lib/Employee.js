//defined + exported Employee class
module.exports = class Employee {
    constructor(name, id, email) {
        this.name = name;
        this.id = id;
        this.email = email;
    }
    getName() {
        return this.name;
    }
    getId() {
        return this.id;
    }
    getEmail() {
        return this.email;
    }
    // getRole => returns Employee
    getRole() {
        return "Employee";
    }
}